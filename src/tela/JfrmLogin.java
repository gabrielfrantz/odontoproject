    public void login() {
        String senha = usuarioDao.criptografa(txtSenha.getText());
        if (usuarioDao.testaUsuario(txtEmail.getText(), senha)) {
            this.dispose();
            JOptionPane.showMessageDialog(null, "Login efetuado com sucesso!");
            this.usuarioAtivo = usuarioDao.getUsuarioAtivo(1);
            System.out.println(this.usuarioAtivo);
            JfrmPrincipal tela = new JfrmPrincipal();
            tela.setExtendedState(MAXIMIZED_BOTH);
            tela.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "E-mail ou senha incorretos!");
            txtEmail.setText("");
            txtSenha.setText("");
            txtEmail.requestFocus();
        }
    }

